public class Combi {
private String Usuarui,Lat,Lon,Contraseña,RutaAsignada,Numero;

    public Combi() {
    }

    public String getUsuarui() {
        return Usuarui;
    }

    public void setUsuarui(String usuarui) {
        Usuarui = usuarui;
    }

    public String getLat() {
        return Lat;
    }

    public void setLat(String lat) {
        Lat = lat;
    }

    public String getLon() {
        return Lon;
    }

    public void setLon(String lon) {
        Lon = lon;
    }

    public String getContraseña() {
        return Contraseña;
    }

    public void setContraseña(String contraseña) {
        Contraseña = contraseña;
    }

    public String getRutaAsignada() {
        return RutaAsignada;
    }

    public void setRutaAsignada(String rutaAsignada) {
        RutaAsignada = rutaAsignada;
    }

    public String getNumero() {
        return Numero;
    }

    public void setNumero(String numero) {
        Numero = numero;
    }
}
